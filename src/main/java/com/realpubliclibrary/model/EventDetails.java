package com.realpubliclibrary.model;

public class EventDetails {
	
	private String eventType;
	
	private String eventName;
	
	private String eventId;
	
	private String startDate;
	
	private String endDate;
	
	private String genre;
	
	private String exhibitionExpense;
	
	private String isFutureEvent;
	

	public String getEventType() {
		return eventType;
	}

	public void setEventType(String eventType) {
		this.eventType = eventType;
	}

	public String getEventName() {
		return eventName;
	}

	public void setEventName(String eventName) {
		this.eventName = eventName;
	}

	public String getEventId() {
		return eventId;
	}

	public void setEventId(String eventId) {
		this.eventId = eventId;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getGenre() {
		return genre;
	}

	public void setGenre(String genre) {
		this.genre = genre;
	}

	public String getExhibitionExpense() {
		return exhibitionExpense;
	}

	public void setExhibitionExpense(String exhibitionExpense) {
		this.exhibitionExpense = exhibitionExpense;
	}

	public String getIsFutureEvent() {
		return isFutureEvent;
	}

	public void setIsFutureEvent(String isFutureEvent) {
		this.isFutureEvent = isFutureEvent;
	}

}
